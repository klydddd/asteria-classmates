"""Benchmark orchestration and report generation."""

from __future__ import annotations

import textwrap
from collections.abc import Callable
from datetime import datetime, timezone
from pathlib import Path

from pydantic import BaseModel

from bosesph.asr import (
    ASRError,
    BenchmarkMetrics,
    TranscriptionResult,
    evaluate_predictions,
    load_model,
    transcribe_split,
)


class BenchmarkReport(BaseModel):
    """Complete result of a benchmark run."""

    dataset_dir: str
    output_dir: str
    model_name: str
    language: str
    split: str
    metrics: BenchmarkMetrics
    predictions_path: str
    results_path: str
    report_path: str


def _per_clip_wer(result: TranscriptionResult) -> float:
    """Compute WER for a single clip (for error-example ranking)."""
    try:
        jiwer = __import__("jiwer")
    except ImportError:
        return 0.0

    from bosesph.asr import normalize_for_scoring

    ref = normalize_for_scoring(result.reference)
    pred = normalize_for_scoring(result.prediction)
    if not ref:
        return 0.0
    return float(jiwer.wer(ref, pred))


def generate_benchmark_report(
    metrics: BenchmarkMetrics,
    predictions: list[TranscriptionResult],
    output_path: str | Path,
) -> Path:
    """Write a human-readable benchmark report in markdown."""
    out = Path(output_path)

    # Rank clips by per-clip WER descending for error examples.
    scored = sorted(
        [(result, _per_clip_wer(result)) for result in predictions],
        key=lambda pair: -pair[1],
    )
    top_errors = scored[:10]

    error_table_rows = []
    for result, clip_wer in top_errors:
        ref_short = result.reference[:60] + ("…" if len(result.reference) > 60 else "")
        pred_short = result.prediction[:60] + (
            "…" if len(result.prediction) > 60 else ""
        )
        error_table_rows.append(
            f"| {result.audio_id} | {ref_short} | {pred_short} | {clip_wer:.2f} |"
        )
    if error_table_rows:
        error_table = "\n".join(error_table_rows)
    else:
        error_table = "| — | — | — | — |"

    report = textwrap.dedent(
        f"""\
        # BosesPH Benchmark Report

        ## Model

        | Field | Value |
        |---|---|
        | Model | {metrics.model} |
        | Language | {metrics.language} |

        ## Dataset

        | Field | Value |
        |---|---|
        | Test clips | {metrics.test_clips} |
        | Total duration | {metrics.total_duration_seconds:.1f}s |

        ## Results

        | Metric | Score |
        |---|---|
        | WER (Word Error Rate) | {metrics.wer:.4f} |
        | CER (Character Error Rate) | {metrics.cer:.4f} |

        ## Highest-Error Examples

        | Audio ID | Reference | Prediction | WER |
        |---|---|---|---:|
        {error_table}

        ## Limitations

        - The base Whisper model does not include Kapampangan as a supported
          language. The baseline uses auto-detection, which will likely fall back
          to a related language (e.g. Tagalog). High error rates are expected and
          serve as the motivation for Phase 6 fine-tuning.
        - WER/CER are computed after lowercasing and stripping punctuation and
          annotation tags. Scores are therefore conservative compared to
          case-sensitive evaluation.
        - Small test sets may produce noisy metrics.

        ---

        *Generated by BosesPH Toolkit on \
{datetime.now(timezone.utc).strftime("%Y-%m-%d")}*
    """
    )

    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(report, encoding="utf-8")
    return out


def generate_comparison_report(
    baseline: BenchmarkMetrics,
    finetuned: BenchmarkMetrics,
    output_path: str | Path,
) -> Path:
    """Write a baseline-vs-fine-tuned comparison report in markdown."""
    out = Path(output_path)

    wer_delta = finetuned.wer - baseline.wer
    cer_delta = finetuned.cer - baseline.cer
    wer_sign = "+" if wer_delta >= 0 else ""
    cer_sign = "+" if cer_delta >= 0 else ""

    if wer_delta < 0:
        verdict = "Fine-tuning **improved** WER."
    elif wer_delta > 0:
        verdict = "Fine-tuning **regressed** WER."
    else:
        verdict = "WER is **unchanged** after fine-tuning."

    report = textwrap.dedent(
        f"""\
        # BosesPH Model Comparison

        ## Models

        | Field | Baseline | Fine-tuned |
        |---|---|---|
        | Model | {baseline.model} | {finetuned.model} |
        | Language | {baseline.language} | {finetuned.language} |
        | Test clips | {baseline.test_clips} | {finetuned.test_clips} |

        ## Results

        | Metric | Baseline | Fine-tuned | Delta |
        |---|---:|---:|---:|
        | WER | {baseline.wer:.4f} | {finetuned.wer:.4f} | {wer_sign}{wer_delta:.4f} |
        | CER | {baseline.cer:.4f} | {finetuned.cer:.4f} | {cer_sign}{cer_delta:.4f} |

        ## Verdict

        {verdict}

        ## Notes

        - Negative deltas indicate improvement (lower error rate).
        - Scores are computed after lowercasing and stripping punctuation and
          annotation tags for fair comparison.
        - The fine-tuned model uses a proxy language token (Tagalog) because
          Kapampangan is not natively supported by Whisper.

        ---

        *Generated by BosesPH Toolkit on \
{datetime.now(timezone.utc).strftime("%Y-%m-%d")}*
    """
    )

    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(report, encoding="utf-8")
    return out


def run_baseline_benchmark(
    dataset_dir: str | Path,
    output_dir: str | Path,
    *,
    model_name: str = "openai/whisper-small",
    language: str | None = None,
    split: str = "test",
    progress_fn: Callable[[int, int], None] | None = None,
) -> BenchmarkReport:
    """Run a full baseline benchmark: transcribe a split and evaluate."""
    ds_dir = Path(dataset_dir)
    out_dir = Path(output_dir)

    split_csv = ds_dir / f"{split}.csv"
    if not split_csv.is_file():
        raise ASRError(f"{split}.csv not found in dataset directory: {ds_dir}")

    out_dir.mkdir(parents=True, exist_ok=True)
    predictions_path = out_dir / "baseline_predictions.csv"
    results_path = out_dir / "results.json"
    report_path = out_dir / "report.md"

    # Load model.
    pipe = load_model(model_name)

    # Transcribe the split.
    predictions = transcribe_split(
        split_csv,
        ds_dir,
        pipe,
        language=language,
        output_path=predictions_path,
        progress_fn=progress_fn,
    )

    # Evaluate.
    metrics = evaluate_predictions(
        predictions_path,
        model=model_name,
        language=language or "kapampangan",
    )

    # Write results JSON.
    results_path.write_text(
        metrics.model_dump_json(indent=2) + "\n",
        encoding="utf-8",
    )

    # Generate report.
    generate_benchmark_report(metrics, predictions, report_path)

    return BenchmarkReport(
        dataset_dir=str(ds_dir),
        output_dir=str(out_dir),
        model_name=model_name,
        language=language or "kapampangan",
        split=split,
        metrics=metrics,
        predictions_path=str(predictions_path),
        results_path=str(results_path),
        report_path=str(report_path),
    )
